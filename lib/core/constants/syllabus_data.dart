  static List<String> getSubjects(String exam, String langCode) {
    final Map<String, Map<String, List<String>>> syllabusMap =
        langCode == 'te' ? examSyllabusTe : examSyllabusEn;
    return syllabusMap[exam]?.keys.toList() ?? [];
  }

  static List<String> getTopics(String exam, String subject, String langCode) {
    final Map<String, Map<String, List<String>>> syllabusMap =
        langCode == 'te' ? examSyllabusTe : examSyllabusEn;
    return syllabusMap[exam]?[subject] ?? [];
  }
